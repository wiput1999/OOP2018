package lab2;

public class VariableDeclaration {

	public static void main(String args[]) {
		int i = 3;
		long l = 1000;
		float f = 3.14f;
		double d = 31.4;
		char c = 4;
		boolean b = true;
		String title = "Java Programming";

		System.out.printf("%d %d %f %f %c %b %s", i, l, f, d, c, b, title);
	}

}
